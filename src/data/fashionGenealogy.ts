const createTimestamp = () => new Date();

const findId = (
  items: { id: string; name: string }[],
  name: string
): string => {
  const item = items.find((item) => item.name === name);
  if (!item) {
    throw new Error(`Could not find item with name: ${name}`);
  }
  return item.id;
};

